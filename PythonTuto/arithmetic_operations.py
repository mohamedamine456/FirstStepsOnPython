# addition
print(10 + 3)

# substruction
print(10 - 3)

#multiplication
print(10 * 3)

# division
print(10 / 3) # returns float
print(10 // 3) # returns int

# reminder
print(10 % 3)

# power
print(10 ** 3)

# increment variable
x = 10
x = x + 3
# or we do
x -= 3
x += 3
print(x)

# priority after the () we have 1=>**, 2=>* | /, 3=> + | -